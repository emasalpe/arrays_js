// Crea un array con elementos
let array = [5, 2, 8, 1, 9];
console.log(array); // [5, 2, 8, 1, 9]
// Eliminar primer valor
array.shift();
console.log(array); // [2, 8, 1, 9]
// Eliminar ultimo valor
array.pop();
console.log(array); // [2, 8, 1]
// Mostrar el indice 2
console.log(array[2]); // 1
// Mostrar indexof
console.log(array)
console.log(`En el indice nro 1 se encuentra el valor: ${array.indexOf(1)}`); // 1
